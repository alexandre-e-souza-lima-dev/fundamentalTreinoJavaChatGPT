package bloco_02;
/*
Contador com for Enunciado:
Crie a classe Ex08 no pacote bloco_02
Use um for para imprimir os números de 1 a 10
Saída esperada (uma por linha):
1
2
3
...
10
Regras:
✔ Use for
❌ Não use while
*/

/**
 * The type Ex 08.
 */
public class Ex08 {

  /**
   * Rodar.
   */
  public static void rodar() {
    System.out.println("Rodado exercício 08!");
    System.out.println("Contador com for");

    for (int i = 1; i <= 10; i++) {
      System.out.println(i);
    }
  }
}
