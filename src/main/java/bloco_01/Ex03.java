package bloco_01;
/*
Operações Matemáticas
Objetivo: praticar operadores aritméticos.
Declare:
int a = 10
int b = 3
Calcule e imprima:
soma
subtração
multiplicação
divisão inteira
*/

public class Ex03 {
  public static void rodar() {
    System.out.println("Rodando exercício 03!");
    int a = 10;
    int b = 3;

    System.out.println("10 + 3 = " + (a + b));
    System.out.println("10 - 3 = " + (a - b));
    System.out.println("10 * 3 = " + (a * b));
    System.out.println("10 / 3 = " + (a / b));
  }

}
